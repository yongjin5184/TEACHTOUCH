package kr.co.edu.hansung.vo;

public class MemberVO {
	private int mbNo;		
	private String mbId;		
	private String mbPassword;	
	private String mbName;		
	private String mbJob;		
	private String mbEmail;	
	private String mbPhone;	
	private String mbAddr;		
	private String mbSex;
	private String mbPicture;
	private String mbRegisterId;
	
	public int getMbNo() {
		return mbNo;
	}
	public void setMbNo(int mbNo) {
		this.mbNo = mbNo;
	}
	public String getMbId() {
		return mbId;
	}
	public void setMbId(String mbId) {
		this.mbId = mbId;
	}
	public String getMbPassword() {
		return mbPassword;
	}
	public void setMbPassword(String mbPassword) {
		this.mbPassword = mbPassword;
	}
	public String getMbName() {
		return mbName;
	}
	public void setMbName(String mbName) {
		this.mbName = mbName;
	}
	public String getMbJob() {
		return mbJob;
	}
	public void setMbJob(String mbJob) {
		this.mbJob = mbJob;
	}
	public String getMbEmail() {
		return mbEmail;
	}
	public void setMbEmail(String mbEmail) {
		this.mbEmail = mbEmail;
	}
	public String getMbPhone() {
		return mbPhone;
	}
	public void setMbPhone(String mbPhone) {
		this.mbPhone = mbPhone;
	}
	public String getMbAddr() {
		return mbAddr;
	}
	public void setMbAddr(String mbAddr) {
		this.mbAddr = mbAddr;
	}
	public String getMbSex() {
		return mbSex;
	}
	public void setMbSex(String mbSex) {
		this.mbSex = mbSex;
	}
	public String getMbPicture() {
		return mbPicture;
	}
	public void setMbPicture(String mbPicture) {
		this.mbPicture = mbPicture;
	}
	public String getMbRegisterId() {
		return mbRegisterId;
	}
	public void setMbRegisterId(String mbRegisterId) {
		this.mbRegisterId = mbRegisterId;
	}
}
